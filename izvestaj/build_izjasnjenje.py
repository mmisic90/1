SEP = '_' * 78

def para(align=None, left=None, hanging=None, after=None, indent_fl=None):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    if align is not None:
        p.alignment = align
    if left is not None:
        pf.left_indent = Twips(left)
    if hanging is not None:
        pf.first_line_indent = Twips(-hanging)
    if after is not None:
        pf.space_after = Pt(after)
    if indent_fl is not None:
        pf.first_line_indent = Twips(indent_fl)
    return p

def run(p, text, bold=False, italic=False, size=None, underline=False):
    r = p.add_run(text)
    r.bold = bold
    r.italic = italic
    r.underline = underline
    if size:
        r.font.size = Pt(size)
    return r

def body(text, runs=None, align=WD_ALIGN_PARAGRAPH.JUSTIFY, **kw):
    p = para(align=align, **kw)
    if runs:
        for t, b, i in runs:
            run(p, t, bold=b, italic=i)
    else:
        run(p, text)
    return p

def separator():
    p = para(align=WD_ALIGN_PARAGRAPH.LEFT, after=8)
    run(p, SEP)

def mini_naslov(text):
    """12pt regular, centriran, između dve linije (sekcija 20)."""
    separator()
    p = para(align=WD_ALIGN_PARAGRAPH.CENTER, after=8)
    run(p, text)
    separator()

def citat_zakona(uvod, citat):
    """Sekcija 22: uvodna rečenica + doslovni citat u italic, ,,...“, indent 720."""
    body(uvod)
    p = para(align=WD_ALIGN_PARAGRAPH.JUSTIFY, left=720)
    run(p, ',,' + citat + '“', italic=True)

def dokaz_blok(stavke, label='ДОКАЗ:'):
    """Sekcija 6.2: hanging 1440, ДОКАЗ: bold, stavke italic, svaka svoj pasus."""
    first = True
    for s in stavke:
        p = para(left=1440, hanging=1440 if first else None, after=2)
        if first:
            run(p, label, bold=True)
            p.add_run('\t')
            first = False
        else:
            p.paragraph_format.first_line_indent = Twips(0)
        run(p, s, italic=True)
    doc.add_paragraph()

